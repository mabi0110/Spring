package com.example.studentmanager.model;

public class Status {

    private String name;

    public Status() {
    }


    public Status(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
